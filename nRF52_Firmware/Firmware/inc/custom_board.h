// PIN Definitions

#define PIN_V_BAT 		2	// Battery voltage after resistance divisor
#define PIN_V_BAT_EN	3	// Pin to enable battery voltage measurement
#define PIN_LED_EN 		4	// Pin to enable boost converter for led voltage
#define PIN_SPI_SPID 	11
#define PIN_PWM2 		12
#define PIN_PWM1 		13
#define PIN_SCL 		22	// I2C SCL signal for sensors
#define PIN_SDA 		23	// I2C SDA signal for sensors
#define PIN_INT1 		20	// Interrupt pin 1 for acc sensor
#define PIN_INT2 		24	// Interrupt pin 2 for acc sensor
#define PIN_SPI_SCK 	18
#define PIN_SPI_CSN 	17
#define PIN_CHG 		31	// Battery charging indication pin

